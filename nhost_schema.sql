-- Função Helper para pegar o ID do usuário atual (Nhost/Hasura)
create or replace function get_current_user_id()
returns uuid
language sql
stable
as $$
  select nullif(current_setting('request.jwt.claim.sub', true), '')::uuid;
$$;

-- Função Helper para pegar o tenant_id do usuário atual
create or replace function get_auth_tenant_id()
returns bigint
language sql
security definer
stable
as $$
  select tenant_id from public.profiles where id = get_current_user_id();
$$;

-- Função Helper para verificar se é Service Role (Admin Global do Hasura)
-- O Hasura envia 'x-hasura-role' = 'admin' quando usamos a admin secret
create or replace function is_admin()
returns boolean
language sql
stable
as $$
  select current_setting('request.jwt.claim.role', true) = 'service_role' 
      or current_setting('hasura.role', true) = 'admin';
$$;

-- 1. TENANTS POLICIES
create policy "Users can view own tenant" on public.tenants
  for select using (
    id = get_auth_tenant_id() 
    or is_admin()
  );

-- 2. PROFILES POLICIES
create policy "Users can view profiles in same tenant" on public.profiles
  for select using (
    tenant_id = get_auth_tenant_id()
    or id = get_current_user_id()
    or is_admin()
  );

create policy "Users can update own profile" on public.profiles
  for update using (id = get_current_user_id());

-- 3. PRODUCTS POLICIES
create policy "View products same tenant" on public.products
  for select using (
    tenant_id = get_auth_tenant_id()
    or is_admin()
  );

create policy "Manage products same tenant" on public.products
  for all using (
    tenant_id = get_auth_tenant_id()
    or is_admin()
  );

-- Public access for Online Store (Leitura pública)
-- No Nhost/Hasura, usuários anônimos têm role 'public' ou 'anonymous'
-- Assumindo que a role anônima não tem tenant_id, permitimos select irrestrito
-- ou filtrado via API. Para RLS nativo, permitimos tudo no SELECT se for público.
create policy "Public view products" on public.products
  for select using (true);

-- 4. CUSTOMERS POLICIES
create policy "Manage customers same tenant" on public.customers
  for all using (
    tenant_id = get_auth_tenant_id()
    or is_admin()
  );

-- 5. SUPPLIERS POLICIES
create policy "Manage suppliers same tenant" on public.suppliers
  for all using (
    tenant_id = get_auth_tenant_id()
    or is_admin()
  );

-- 6. ORDERS POLICIES
create policy "Manage orders same tenant" on public.orders
  for all using (
    tenant_id = get_auth_tenant_id()
    or is_admin()
  );

create policy "Public create orders" on public.orders
  for insert with check (true);

-- 7. ORDER ITEMS POLICIES
create policy "Manage order items same tenant" on public.order_items
  for all using (
    exists (
      select 1 from public.orders
      where public.orders.id = public.order_items.order_id
      and (public.orders.tenant_id = get_auth_tenant_id() or is_admin())
    )
  );

create policy "Public create order items" on public.order_items
  for insert with check (true);

-- 8. TRANSACTIONS POLICIES
create policy "Manage transactions same tenant" on public.transactions

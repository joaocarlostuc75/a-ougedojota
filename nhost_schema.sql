-- Trigger para criar perfil automaticamente ao criar usuário no Auth (Nhost)
-- A tabela de usuários no Nhost é auth.users
create or replace function public.handle_new_nhost_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, tenant_id, username, name, role)
  values (
    new.id,
    (new.metadata->>'tenant_id')::bigint,
    new.metadata->>'username',
    new.display_name,
    coalesce(new.metadata->>'role', 'cashier')
  );
  return new;
end;
$$;

-- Nota: No Nhost, você deve garantir que tem permissão para criar triggers no schema auth.
-- Caso contrário, use Hasura Event Triggers.
-- Tentativa de criar trigger nativo (geralmente funciona se for superuser do DB):
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_nhost_user();

